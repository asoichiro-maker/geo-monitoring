def main():
    if not SAMPLES or SAMPLES[0]["response_text"] == "PASTE_ACTUAL_AI_RESPONSE_HERE":
        print("SAMPLES に実際のAI回答テキストと人力判定を入力してから実行してください。")
        return

    results = []
    correct_mention = 0
    correct_sentiment = 0

    for sample in SAMPLES:
        try:
            judged = judge(sample)
        except Exception as e:
            print(f"[ID {sample['id']}] エラー: {e}")
            continue

        mention_match = judged["is_mentioned"] == sample["human_is_mentioned"]
        sentiment_match = judged["sentiment"] == sample["human_sentiment"]
        correct_mention += mention_match
        correct_sentiment += sentiment_match

        results.append({
            "id": sample["id"],
            "query": sample["query"],
            "claude_is_mentioned": judged["is_mentioned"],
            "human_is_mentioned": sample["human_is_mentioned"],
            "mention_match": mention_match,
            "claude_sentiment": judged["sentiment"],
            "human_sentiment": sample["human_sentiment"],
            "sentiment_match": sentiment_match,
            "note": sample.get("note", ""),
        })
        print(f"[ID {sample['id']}] 言及一致: {mention_match} / トーン一致: {sentiment_match}")
        if sample.get("note"):
            print(f"   note: {sample['note']}")

    n = len(results)
    if n == 0:
        print("有効な結果がありませんでした。")
        return

    mention_rate = correct_mention / n * 100
    sentiment_rate = correct_sentiment / n * 100

    print("\n=== 判定結果サマリー ===")
    print(f"サンプル数: {n}")
    print(f"言及有無 一致率: {mention_rate:.1f}%")
